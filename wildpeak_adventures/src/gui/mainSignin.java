    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed

        if (jToggleButton1.isSelected()) {
            jPasswordField1.setEchoChar((char) 0);

            // Load the "show eye" icon
            java.net.URL imgURL = getClass().getResource("/resosurcs/show eye.png");
            if (imgURL != null) {
                jToggleButton1.setIcon(new javax.swing.ImageIcon(imgURL));
            } else {
                System.err.println("Couldn't find file: show eye.png");
            }

        } else {
            jPasswordField1.setEchoChar('*');  // Reset to password masking

            // Load the "hide eye" icon
            java.net.URL imgURL = getClass().getResource("/resosurcs/hide eye.png");
            if (imgURL != null) {
                jToggleButton1.setIcon(new javax.swing.ImageIcon(imgURL));
            } else {
                System.err.println("Couldn't find file: hide eye.png");
            }

        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

