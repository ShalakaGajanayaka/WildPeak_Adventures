    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            String district = jTextField1.getText();

            if (district.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter District", "Information", JOptionPane.INFORMATION_MESSAGE);
            } else {

                ResultSet resultSet = MYSQL.executeSearch("SELECT * FROM `district` WHERE `name` = '" + district + "'");
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This District is Already Added.", "Information", JOptionPane.INFORMATION_MESSAGE);
                    return; // Exit the method if an address already exists
                } else {
                    MYSQL.executeIUD("INSERT INTO `district`(`name`)"
                            + "VALUES('" + district + "')");

                    loadDistrict();
                    reset();
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
//            logger.log(Level.WARNING, "Exception In Address View in Add Button", e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

